// Obtener ID del vuelo seleccionado
public int getVueloSeleccionado() {
    int fila = tblVuelos.getSelectedRow();
    if (fila >= 0) {
        return Integer.parseInt(tblVuelos.getValueAt(fila, 0).toString());
    }
    return -1; // Ning√∫n vuelo seleccionado
} 
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
  
